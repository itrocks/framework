<?php
namespace ITRocks\Framework\Dao\Mysql;

/**
 * A Mysql process, as shown by SHOW [FULL] PROCESSLIST
 */
class Process
{

	//-------------------------------------------------------------------------------------- $Command
	/**
	 * @var string
	 */
	protected $Command;

	//----------------------------------------------------------------------------------------- $Host
	/**
	 * @var string
	 */
	protected $Host;

	//------------------------------------------------------------------------------------------- $Id
	/**
	 * @var integer
	 */
	protected $Id;

	//----------------------------------------------------------------------------------------- $Info
	/**
	 * @var string
	 */
	protected $Info;

	//---------------------------------------------------------------------------------------- $State
	/**
	 * @var string
	 */
	protected $State;

	//----------------------------------------------------------------------------------------- $Time
	/**
	 * @var integer
	 */
	protected $Time;

	//----------------------------------------------------------------------------------------- $User
	/**
	 * @var string
	 */
	protected $User;

	//------------------------------------------------------------------------------------------- $db
	/**
	 * @var string
	 */
	protected $db;

	//------------------------------------------------------------------------------ getMysqlThreadId
	/**
	 * @return integer
	 */
	public function getMysqlThreadId()
	{
		return $this->Id;
	}

}
