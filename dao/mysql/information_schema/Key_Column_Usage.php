	 * The foreign column name (ie always id when it is generated by the framework)
	 *
	 * @values id
	 * @var string
	 */
	private $REFERENCED_COLUMN_NAME;

	//------------------------------------------------------------------------ $REFERENCED_TABLE_NAME
	/**
	 * The foreign table name
	 *
	 * @var string
	 */
	private $REFERENCED_TABLE_NAME;

	//---------------------------------------------------------------------- $REFERENCED_TABLE_SCHEMA
	/**
	 * The foreign database name
	 *
	 * @var string
	 */
	private $REFERENCED_TABLE_SCHEMA;

	//-------------------------------------------------------------------------------- $TABLE_CATALOG
	/**
	 * @values def
	 * @var string
	 */
	private $TABLE_CATALOG;

	//----------------------------------------------------------------------------------- $TABLE_NAME
	/**
	 * The source table name
	 *
	 * @var string
	 */
	private $TABLE_NAME;

	//--------------------------------------------------------------------------------- $TABLE_SCHEMA
	/**
	 * The source database name
	 *
	 * @var string
	 */
	private $TABLE_SCHEMA;

}
